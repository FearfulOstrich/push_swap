int	ft_atoi(const char *str)
{
	int	nbr;
	int	sgn;

	nbr = 0;
	sgn = 1;
	while ((*str == ' ') || (((*str >= 9) && (*str <= 13))))
		str++;
	if ((*str == '-') || (*str == '+'))
	{
		if (*str == '-')
			sgn *= -1;
		str++;
	}
	while ((*str >= '0') && (*str <= '9'))
	{
		nbr = nbr * 10 + (*str - '0');
		str++;
	}
	return (sgn * nbr);
}
