#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			cpt;
	unsigned char	*str1;
	unsigned char	*str2;

	str1 = (unsigned char *) s1;
	str2 = (unsigned char *) s2;
	if (n == 0)
		return (0);
	cpt = 0;
	while (str1[cpt] == str2[cpt] && cpt < n - 1)
		cpt++;
	return (str1[cpt] - str2[cpt]);
}
