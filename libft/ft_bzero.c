#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	cpt;
	char	*str;

	if (n == 0)
		return ;
	cpt = 0;
	str = (char *) s;
	while (cpt < n)
		str[cpt++] = 0;
	return ;
}
